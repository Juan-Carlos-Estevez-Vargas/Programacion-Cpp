#include <iostream>
using namespace std;

int main() {
    // Tipo de dato entero
    int x = 10;
    cout << x << endl;

    // Tipo de dato flotante
    float y = 3.14;
    cout << y << endl;

    // Tipo de dato doble
    double z = 2.71828;
    cout << z << endl;

    // Tipo de dato booleano
    bool b = true;
    cout << b << endl;

    // Tipo de dato caracter
    char c = 'a';
    cout << c << endl;

    // Tipo de dato string
    string s = "Hello, World!";
    cout << s << endl;

    // Tipo de dato enum
    enum Color {RED, GREEN, BLUE};
    Color color = RED;
    Color color2 = GREEN;
    cout << color << endl;

    return 0;
}